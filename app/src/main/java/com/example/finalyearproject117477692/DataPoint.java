package com.example.finalyearproject117477692;

// class used for chart data

public class DataPoint {

    int xValue;
    int yValue;

    public DataPoint(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public DataPoint() {
    }

    public int getxValue() {
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }




}
